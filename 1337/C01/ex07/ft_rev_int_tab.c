#include <unistd.h>

void	ft_swap(int *a, int *b)
{
	int	temp;

	temp = *a;
	*a = *b;
	*b = temp;
}

void	ft_rev_int_tab(int *tab, int size)
{
	int	i;

	i = 0;
	size --;
	while (i <= size)
	{
		ft_swap(&tab[i++], &tab[size--]);
	}
}
